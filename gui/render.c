void render_rect(Framebuffer* fb, u32 x, u32 y, u32 w, u32 h, u32 color)
{
    for (u32 yy = y; yy < y + h; yy++)
    {
        for (u32 xx = x; xx < x + w; xx++)
        {
            framebuffer_put_pixel(fb, xx, yy, color);
        }
    }
}

void render_char(Framebuffer* fb, u32 x, u32 y, char c, u32 fg, u32 bg)
{
    if (c < 0 || c > 127)
    {
        return;
    }
    const unsigned char* glyph = font_data[(int)c];
    for (u32 row = 0; row < 8; row++)
    {
        unsigned char bits = glyph[row];
        for (u32 col = 0; col < 8; col++)
        {
            u32 color = (bits & (1 << (7 - col))) ? fg : bg;
            framebuffer_put_pixel(fb, x + col, y + row, color);
        }
    }
}

void render_text(Framebuffer* fb, u32 x, u32 y, const char* text, u32 fg, u32 bg)
{
    u32 cx = x;
    for (u32 i = 0; text[i] != 0; i++)
    {
        char c = text[i];
        if (c == '\n')
        {
            y += 8;
            cx = x;
        }
        else
        {
            render_char(fb, cx, y, c, fg, bg);
            cx += 8;
        }
    }
}

